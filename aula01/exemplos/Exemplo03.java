package exemplos;

public class Exemplo03 {

    public static void main(String[] args) {
        int numero;
        numero = 4;

        System.out.println("numero");
        System.out.println(numero);

        numero = 5;

        System.out.println(numero);

        numero = 11 * 3;

        System.out.println(numero);

        System.out.println(numero + 2);
        System.out.println(numero);

        numero = numero + 2;
        System.out.println(numero);
    }
}
